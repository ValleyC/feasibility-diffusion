"""
Principled denoising training for feasibility-preserving diffusion.

This implements the PROPER discrete denoising diffusion objective:

  Forward process:
    Given a clean tour x_0, apply T random 2-opt swaps to get x_T.
    Record the full trajectory and which swap was applied at each step.

  Training objective:
    Given (x_t, t), predict which swap was applied at step t.
    Equivalently: predict how to reverse the last noise step.
    Since 2-opt is an involution (applying the same swap undoes it),
    predicting the applied swap = predicting how to denoise.

  Inference:
    Start from random tour x_T. For t = T, T-1, ..., 1:
    model predicts swap → apply it → one step closer to clean tour.

This is the discrete analog of DDPM's "predict the noise ε" objective.

Label-free: x_0 is generated by greedy 2-opt (no external solver).
The label (which swap was applied) comes from the forward process itself.

Usage:
    python -m training.denoise_trainer --N 50 --device cuda:0
    python -m training.denoise_trainer --N 50 --device cuda:0 --train_data path/to/edisco_data.txt
"""

